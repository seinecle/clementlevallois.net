  <h1>Gephi plugin to sort nodes vertically, according to the alphabetical order of their labels.</h1>
  <br />
  <h3>Why this plugin?</h3>Very often, isolated nodes (those without any connection) are scattered all over the place. When
  printing a poster, that is quite unaesthetic.
  <br />This plugin orders isolated nodes vertically, in the alphabetical order of their labels. It makes for a much better
  presentation of the information.
  <br />
  <br />
  <h3>To install in Gephi:</h3>
  <p>- <a href="https://marketplace.gephi.org/how-to-install-a-plugin/">follow these steps</a>
  <p>- then don't forget to stop and relaunch Gephi.</a>
  
  
  <h3>How to use:</h3>
  <p>- find the alphabetical sorter layout in the layout panel.
  <br />- apply it!
